    private void txtTutorFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtTutorFocusLost
        try {
            cmbAnimal.removeAllItems();
            if (txtTutor.getText().equals(""))
                return;            
             
            List<Cliente> result = control.listar(txtTutor.getText());
            
            
            if (result.size() > 0){
                cliente = result.get(0);
                //procura e adiciona os animais
                cliente = control.procurarAnimais(cliente);
            }
            
            for(Animal animal : cliente.getAnimais()){
                cmbAnimal.addItem(animal);
            }
            cmbAnimal.setSelectedIndex(-1);
            
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro:\n" + ex.getMessage());
        } catch(Exception e){
            
        }
    }//GEN-LAST:event_txtTutorFocusLost

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if (cmbAnimal.getSelectedIndex() != -1){
            Consulta c;
            c = controlConsulta.criarConsulta(consulta.getVeterinario(), consulta.getData(), consulta.getHorario(), 
                cmbAnimal.getItemAt(cmbAnimal.getSelectedIndex()), cliente);            
            
            try {
                //insere os dados no banco
                if(controlConsulta.cadastrarConsulta(c)){
                    JOptionPane.showMessageDialog(this, "Dados inseridos");
                    this.dispose();
                }
                else
                    JOptionPane.showMessageDialog(this, "NÃ£o foi possivel inserir os dados");
                
            } catch (ClassNotFoundException | SQLException ex) {
                JOptionPane.showMessageDialog(this, "Erro:\n" + ex.getMessage());
            }
            System.out.println("agendar consulta");
        }
    }//GEN-LAST:event_jButton1ActionPerformed

