    private void jTabelaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTabelaMouseClicked
        if(jTabela.getSelectedColumn() == 5){ //5 = coluna cancelar
            int linha = jTabela.getSelectedRow();
            Consulta consulta = valoresTabela.get(linha);
            if (consulta.isRealizado()) //verifica se a consulta foi realizada
                JOptionPane.showMessageDialog(this, "Essa consulta j√° foi realizada");
            else{
                int resp = JOptionPane.showConfirmDialog(this, "Deseja realmente desmarcar essa consulta?");
                if (resp == 0){ //sim                
                    try {
                        if(control.desmarcar(consulta)){
                            JOptionPane.showMessageDialog(this, "Consulta desmarcada!");
                            this.btnPesquisarActionPerformed(null);
                        }
                    } catch (SQLException | ClassNotFoundException ex) {
                        JOptionPane.showMessageDialog(this, "erro:\n"+ex.getMessage());
                    }

                }
            }
        }
    }//GEN-LAST:event_jTabelaMouseClicked

    private void btnPesquisarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnPesquisarActionPerformed
        String data = txtData.getText();
        if (data.equals(""))
            return;
        try {
            limparTable();
            List<Consulta> resp = control.pesquisarPorData(new Data(data));
            valoresTabela = resp;//passa para estrutura
            DefaultTableModel model = (DefaultTableModel) jTabela.getModel();
            
            for (Consulta c : resp){
                String vet = c.getVeterinario() == null ? "" : c.getVeterinario().getNome();
                String animal = c.getAnimal() == null ? "" : c.getAnimal().getNome();
                String cliente = c.getCliente() == null ? "" : c.getCliente().getNome();
                String[] dados = {
                    c.getData().toString(), 
                    c.getHorario(), 
                    vet,
                    cliente, 
                    animal, ""
                };
                model.addRow(dados);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(FrmConsultarAgendamento.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnPesquisarActionPerformed

