    private void btnFinalizarConsultaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFinalizarConsultaActionPerformed
        try {
            //adiciona a observação no objeto
            consulta.setObservacao(txtConsulta.getText());
            //cadastra no banco
            control.finalizarConsulta(consulta);
            JOptionPane.showMessageDialog(this, "Consulta Finalizada");
            this.dispose();
        } catch (ClassNotFoundException | SQLException ex) {
            JOptionPane.showMessageDialog(this, "Erro:\n"+ex.getMessage());
        }
    }//GEN-LAST:event_btnFinalizarConsultaActionPerformed

