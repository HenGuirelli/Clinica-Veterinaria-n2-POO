    public FrmConsulta(Consulta c) {
        this();
        consulta = c;
        txtTutor.setText(c.getCliente().getNome());
        txtData.setText(c.getData().toString());
        txtAnimal.setText(c.getAnimal().getNome());
        txtVeterinario.setText(c.getVeterinario().getNome());
        txtConsulta.setText(c.getObservacao());
        if (c.isRealizado()){
            btnFinalizarConsulta.setEnabled(false);
            btnSolicitarExame.setEnabled(false);
            txtConsulta.setEnabled(false);
            JOptionPane.showMessageDialog(this, "Consulta já realizada");
        }
    }
    
