    public FrmMenu(EnumTipoConta tipo){
        this();
        System.out.println(tipo);
        if (EnumTipoConta.secretaria.equals(tipo)){
            //bloqueia bot√µes de veterinario
            pesquisarConsultaVet.setVisible(false);
            controleContas.setVisible(false);
        }else if (EnumTipoConta.veterinario.equals(tipo)){
            menuVeterinario.setVisible(false);
            menuAnimal.setVisible(false);
            menuCliente.setVisible(false);
            agendarConsulta.setVisible(false);
            pesquisarConsultaSec.setVisible(false);
            controleContas.setVisible(false);
        }
    }

